package pe.com.claro.venta.evaluarreglas.model;

public class ResponsePrecioBase {
	private String codMaterial;
	private String desMaterial;
	private String precioBase;
	private String precioCompra;
	private String tipoMaterial;
	public String getCodMaterial() {
		return codMaterial;
	}
	public void setCodMaterial(String codMaterial) {
		this.codMaterial = codMaterial;
	}
	public String getDesMaterial() {
		return desMaterial;
	}
	public void setDesMaterial(String desMaterial) {
		this.desMaterial = desMaterial;
	}
	public String getPrecioBase() {
		return precioBase;
	}
	public void setPrecioBase(String precioBase) {
		this.precioBase = precioBase;
	}
	public String getPrecioCompra() {
		return precioCompra;
	}
	public void setPrecioCompra(String precioCompra) {
		this.precioCompra = precioCompra;
	}
	public String getTipoMaterial() {
		return tipoMaterial;
	}
	public void setTipoMaterial(String tipoMaterial) {
		this.tipoMaterial = tipoMaterial;
	}
	
	
}
