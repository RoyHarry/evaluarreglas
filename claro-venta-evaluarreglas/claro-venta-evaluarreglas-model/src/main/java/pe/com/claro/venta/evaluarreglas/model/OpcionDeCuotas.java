package pe.com.claro.venta.evaluarreglas.model;

public class OpcionDeCuotas {
	private int cuotaMaxima;
	private String mostrarRespuesta;
	private double topeMaximo;
	public int getCuotaMaxima() {
		return cuotaMaxima;
	}
	public void setCuotaMaxima(int cuotaMaxima) {
		this.cuotaMaxima = cuotaMaxima;
	}
	public String getMostrarRespuesta() {
		return mostrarRespuesta;
	}
	public void setMostrarRespuesta(String mostrarRespuesta) {
		this.mostrarRespuesta = mostrarRespuesta;
	}
	public double getTopeMaximo() {
		return topeMaximo;
	}
	public void setTopeMaximo(double topeMaximo) {
		this.topeMaximo = topeMaximo;
	}
	  
}
